package tools;

public class AdvCalc extends calc{
    // here AdvCalc is sub class(child class) which is using the features of super class(parent class)
    // this is single inheritance
    public int mul(int n1, int n2){
        return n1 * n2;
    }
    public int div(int n1, int n2){
        return n1 / n2;
    }
}
